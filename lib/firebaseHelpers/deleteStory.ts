
import AsyncStorage from '@react-native-async-storage/async-storage';

export async function deleteStory(storyId: string) {
  try {
    const apiBase = process.env.EXPO_PUBLIC_API_BASE_URL || 'https://trave-social-backend.onrender.com/api';
    const fullUrl = `${apiBase}/stories/${storyId}`;
    console.log('[deleteStory] Deleting story at:', fullUrl);
    
    // Get userId from AsyncStorage
    const userId = await AsyncStorage.getItem('userId');
    
    const res = await fetch(fullUrl, { 
      method: 'DELETE',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ userId })
    });
    const data = await res.json();
    
    console.log('[deleteStory] Response:', data);
    return data;
  } catch (error: any) {
    console.error('[deleteStory] Error:', error);
    return { success: false, error: error.message };
  }
}
